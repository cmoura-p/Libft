#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int	x;
	int	y;

	x = 5;
	y = 10;
	ft_swap(&x, &y);
	printf("%d\n", x);
	printf("%d\n", y);
}

