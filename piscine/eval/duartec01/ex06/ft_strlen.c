#include <stdio.h>

int	ft_strlen(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		a++;
	}
	return (a);
}

int	main(void)
{
	char	str[] = "1234567890";
	
	printf("%d\n", ft_strlen(str));
}

