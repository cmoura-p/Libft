#include <unistd.h>

void	ft_putstr(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		write(1, &str[index], 1);
		index++;
	}
}

int	main(void)
{
	char source[] = "Claudia Moura 123456";
	ft_putstr(source);
	return (0);
}

