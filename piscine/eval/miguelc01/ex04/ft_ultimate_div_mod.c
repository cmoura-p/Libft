#include <stdio.h>

void ft_ultimate_div_mod(int *a, int *b);

int main()
{
	int a = 19, b = 4;
	
	ft_ultimate_div_mod(&a,&b);
	printf("Div:%d\nMod:%d", a, b);
	return (0);
}
void	ft_ultimate_div_mod(int *a, int *b)
{
	int	temp_a;
	int	temp_b;

	temp_a = *a;
	temp_b = *b;
	*a = temp_a / temp_b;
	*b = temp_a % temp_b;
}
