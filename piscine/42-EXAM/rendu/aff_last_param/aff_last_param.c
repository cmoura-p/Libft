#include <unistd.h>

int	main(int argc, char **argv)
{
	int	n;
	n = 0;
	if (argc == 1)
		write(1, "\n", 1);
	else
	{
		while (argv[argc-1][n] != '\0')
		{
			write(1, &argv[argc-1][n], 1); 
			n++;
		}
		write(1,"\n",1);
	}
	return (0);
}
